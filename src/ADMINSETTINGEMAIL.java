    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        PreparedStatement mystmt=null;
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hitachi?zeroDateTimeBehavior=convertToNull", "root", "");
            String sql = "Select * from admin where Admin_id=? and Admin_password=?";

            mystmt = con.prepareStatement(sql);
            mystmt.setString(1, user.getText());
            mystmt.setString(2, old.getText());
            ResultSet rs = mystmt.executeQuery();
            String us=user.getText();

            if(rs.next()){
                String query="UPDATE admin SET Email_ID=? WHERE Admin_id=?";
                mystmt= con.prepareStatement(query);
                mystmt.setString(1, mobile.getText());
                mystmt.setString(2, user.getText());
                mystmt.executeUpdate();
                JOptionPane.showMessageDialog(this,"Email Address updation complete","Successful",JOptionPane.INFORMATION_MESSAGE);
                new ADMINHOME(us).setVisible(true);
                dispose();

            }
            else{
                JOptionPane.showMessageDialog(this,"Entered Password is wrong","Anticipated",JOptionPane.ERROR_MESSAGE);
                old.setText("");
                mobile.setText("");

            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);

        }
    }//GEN-LAST:event_jButton3ActionPerformed

